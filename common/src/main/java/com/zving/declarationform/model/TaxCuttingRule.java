package com.zving.declarationform.model;

public class TaxCuttingRule {
	String id;
	String code;
	String largetype;
	String smalltype;
	String way;
	int amountcap;
	float rate;
	String policypapertitle;
	String policypapercontent;
	String starttime;
	String endtime;
	String validitydate;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getLargetype() {
		return largetype;
	}

	public void setLargetype(String largetype) {
		this.largetype = largetype;
	}

	public String getSmalltype() {
		return smalltype;
	}

	public void setSmalltype(String smalltype) {
		this.smalltype = smalltype;
	}

	public String getWay() {
		return way;
	}

	public void setWay(String way) {
		this.way = way;
	}

	public int getAmountcap() {
		return amountcap;
	}

	public void setAmountcap(int amountcap) {
		this.amountcap = amountcap;
	}

	public float getRate() {
		return rate;
	}

	public void setRate(float rate) {
		this.rate = rate;
	}

	public String getPolicypapertitle() {
		return policypapertitle;
	}

	public void setPolicypapertitle(String policypapertitle) {
		this.policypapertitle = policypapertitle;
	}

	public String getPolicypapercontent() {
		return policypapercontent;
	}

	public void setPolicypapercontent(String policypapercontent) {
		this.policypapercontent = policypapercontent;
	}

	public String getStarttime() {
		return starttime;
	}

	public void setStarttime(String starttime) {
		this.starttime = starttime;
	}

	public String getEndtime() {
		return endtime;
	}

	public void setEndtime(String endtime) {
		this.endtime = endtime;
	}

	public String getValiditydate() {
		return validitydate;
	}

	public void setValiditydate(String validitydate) {
		this.validitydate = validitydate;
	}

}
